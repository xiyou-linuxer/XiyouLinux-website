package com.xiyoulinux.model;
public class Donate {
  private int id;
  private String wei_name;
  private String weixin;
  private String alipay_namep;
  private String alipay;

  public Donate() {
  }

  public int getId() {
    return this.id;
  }

  public void setId(int id) {
    this.id = id;
  }

  public String getWei_name() {
    return this.wei_name;
  }

  public void setWei_name(String wei_name) {
    this.wei_name = wei_name;
  }

  public String getWeixin() {
    return this.weixin;
  }

  public void setWeixin(String weixin) {
    this.weixin = weixin;
  }

  public String getAlipay_namep() {
    return this.alipay_namep;
  }

  public void setAlipay_namep(String alipay_namep) {
    this.alipay_namep = alipay_namep;
  }

  public String getAlipay() {
    return this.alipay;
  }

  public void setAlipay(String alipay) {
    this.alipay = alipay;
  }
}
